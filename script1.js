/* Задание 1
Создать переменные num1, num2 которые пользователь вводит с клавиатуры
Проверьте, что переменная num1 равна или меньше 1, а переменная num2 больше или равна 3. */
let num1 = + prompt(`Введи значение переменной num1:`);
if (num1<=1) {alert(`Переменная num1 меньше или равна 1`);}
else {alert(`Переменная num1 больше 1`);}
let num2 = + prompt(`Введи значение переменной num2:`);
if (num2>=3) {alert('Переменная num2 больше или равна 3');}
else {alert('Переменная num2 ментше 3');}